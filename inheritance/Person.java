package inheritance;

public class Person {

	protected String name;
	
	public void walk() {
		System.out.println(name + " Is Walking");
	}
	
	public void eat() {
		System.out.println("person" + name + " Is Eating");
	}

	
		public static void laughing() {
			System.out.println("person is laughing");
		}
}
